// This class will represent x y values to be fired at
// an enemy board
public class Torpedo{
	private int x;
	private int y;

	public Torpedo(int x, int y){
		this.x = x;
		this.y = y;
	}

	public int getX(){
		return this.x;
	}

	public int getY(){
		return this.y;
	}
}